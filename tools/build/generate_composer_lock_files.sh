#!/usr/bin/env bash
set -e -u -o pipefail
#set -x

function show_help() {
    echo "Usage: $0 [optional arguments]"
    echo
    echo "Options:"
    echo "  --keep_temp_files       Optional. Keep temporary files. Default: false (i.e., delete temporary files on both success and failure)."
    echo
    echo "Example:"
    echo "  $0 --keep_temp_files"
}

# Function to parse arguments
function parse_args() {
    export ELASTIC_OTEL_PHP_TOOLS_KEEP_TEMP_FILES="false"

    while [[ "$#" -gt 0 ]]; do
        case $1 in
        --keep_temp_files)
            export ELASTIC_OTEL_PHP_TOOLS_KEEP_TEMP_FILES="true"
            ;;
        --help)
            show_help
            exit 0
            ;;
        *)
            echo "Unknown parameter passed: $1"
            show_help
            exit 1
            ;;
        esac
        shift
    done
}

function generate_files_for_PHP_version() {
    local PHP_version_no_dot="${1:?}"

    local docker_run_cmd_line_args=()
    build_docker_env_vars_command_line_part docker_run_cmd_line_args

    local PHP_version_dot_separated
    PHP_version_dot_separated="$(convert_no_dot_to_dot_separated_version "${PHP_version_no_dot}")"

    echo "Generating files for PHP ${PHP_version_dot_separated} ..."

    local PHP_docker_image
    PHP_docker_image=$(build_light_PHP_docker_image_name_for_version_no_dot "${PHP_version_no_dot}")

    docker run --rm \
        "${docker_run_cmd_line_args[@]}" \
        -v "${repo_temp_copy_dir}:/repo_root:ro" \
        -v "${stage_generated_composer_lock_files_dir}/:/docker_host_dst/stage_generated_composer_lock_files_dir/" \
        -w "/repo_temp_copy_dir" \
        "${PHP_docker_image}" \
        sh -c "\
            apk update && apk add bash rsync \
            && mkdir -p /tmp/repo/  \
            && cp -r /repo_root/. /tmp/repo/ \
            && cd /tmp/repo \
            && ./tools/install_composer.sh \
            && php ./tools/build/generate_composer_files_for_current_PHP_version.php \
            && cp -r //tmp/repo/${elastic_otel_php_generated_composer_lock_files_dir_name:?}/. /docker_host_dst/stage_generated_composer_lock_files_dir/ \
            && chown -R ${current_user_id}:${current_user_group_id} /docker_host_dst/stage_generated_composer_lock_files_dir/ \
            && chmod -R +r,u+w /docker_host_dst/stage_generated_composer_lock_files_dir/ \
        "
}

function on_script_exit() {
    local exit_code=$?

    if [ -n "${repo_temp_copy_dir+x}" ] && [ -d "${repo_temp_copy_dir}" ]; then
        delete_temp_dir "${repo_temp_copy_dir}"
    fi

    cd "${current_dir_to_restore}/"

    exit ${exit_code}
}

function main() {
    current_dir_to_restore="${PWD}"
    work_repo_root_dir="$(realpath "${PWD}")"
    this_script_dir="$(dirname "${BASH_SOURCE[0]}")"
    this_script_dir="$(realpath "${this_script_dir}")"
    src_repo_root_dir="$(realpath "${this_script_dir}/../..")"

    source "${src_repo_root_dir}/tools/shared.sh"

    # Parse arguments
    parse_args "$@"

    current_user_id="$(id -u)"
    current_user_group_id="$(id -g)"

    trap on_script_exit EXIT

    repo_temp_copy_dir="$(mktemp -d)"
    "${src_repo_root_dir}/tools/copy_repo_exclude_generated.sh" "${work_repo_root_dir}" "${repo_temp_copy_dir}"
    cd "${repo_temp_copy_dir}"

    # The script below must be called before any .php scripts in ./tools/build/
    # because .php scripts in ./tools/build/ depend on some of .php source files
    # generated by the script below
    "${src_repo_root_dir}/tools/build/configure_php_templates_if_not_done_yet.sh"

    # composer run-script -- download_and_adapt_packages_to_PHP_81 "${repo_temp_copy_dir}"
    #   expects
    #       - "./composer.json"
    #   creates
    #       - "./${elastic_otel_php_packages_adapted_to_PHP_81_rel_path:?}/"
    #       - "./${elastic_otel_php_composer_home_for_packages_adapted_to_PHP_81_rel_path:?}/config.json"
    php "${src_repo_root_dir}/tools/build/download_adapt_packages_to_PHP_81_and_gen_config.php"

    stage_generated_composer_lock_files_dir="${repo_temp_copy_dir}/${elastic_otel_php_generated_composer_lock_files_dir_name:?}"
    mkdir -p "${stage_generated_composer_lock_files_dir}"

    cp "${repo_temp_copy_dir}/composer.json" "${stage_generated_composer_lock_files_dir}/${elastic_otel_php_generated_composer_files_base_file_name:?}.json"

    for PHP_version_no_dot in "${elastic_otel_php_supported_php_versions[@]:?}" ; do
        generate_files_for_PHP_version "${PHP_version_no_dot}"
    done

    php "${src_repo_root_dir}/tools/build/verify_generated_composer_lock_files.php"

    local dst_generated_composer_lock_files_dir="${work_repo_root_dir}/${elastic_otel_php_generated_composer_lock_files_dir_name:?}"
    mkdir -p "${dst_generated_composer_lock_files_dir}"
    delete_dir_contents "${dst_generated_composer_lock_files_dir}"
    cp -r "${stage_generated_composer_lock_files_dir}/." "${dst_generated_composer_lock_files_dir}/"
    # No need for delete_temp_dir "${repo_temp_copy_dir}" - ${repo_temp_copy_dir} is deleted in on_script_exit()
}

main "$@"
